#include "utils.h"

#include "globals.h"

#include <sys/stat.h>
#include <filesystem>

bool fileExists(const char *filename)
{
    if (ff8)
    {
        // Fastest way
        return std::filesystem::exists(filename);
    }

    struct stat dummy;

    // Use stat to keep compatibility with 7th Heaven
    return stat(filename, &dummy) == 0;
}
